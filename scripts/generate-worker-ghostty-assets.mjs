import { randomUUID } from "node:crypto";
import { readFile, readdir, rename, rm, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const modulePath = fileURLToPath(import.meta.resolve("ghostty-web"));
const distPath = path.dirname(modulePath);
const generatedPath = fileURLToPath(
  new URL("../src/worker-ghostty-assets.generated.ts", import.meta.url),
);
const browserExternalPath = await findBrowserExternalAsset(distPath);
const assets = [
  {
    key: "module",
    path: modulePath,
    contentType: "text/javascript; charset=utf-8",
  },
  {
    key: "wasm",
    path: fileURLToPath(import.meta.resolve("ghostty-web/ghostty-vt.wasm")),
    contentType: "application/wasm",
  },
  {
    key: "browserExternal",
    path: browserExternalPath,
    contentType: "text/javascript; charset=utf-8",
  },
];

const renderedAssets = await Promise.all(
  assets.map(async ({ key, path: assetPath, contentType }) => {
    const body = await readFile(assetPath);
    const base64 = JSON.stringify(body.toString("base64"));
    return [
      `  [GHOSTTY_ASSET_PATHS.${key}]: {`,
      ...(base64.length > 80 ? [`    base64:`, `      ${base64},`] : [`    base64: ${base64},`]),
      `    contentType: ${JSON.stringify(contentType)},`,
      `  },`,
    ].join("\n");
  }),
);

const temporaryPath = `${generatedPath}.${process.pid}.${randomUUID()}.tmp`;
try {
  await writeFile(
    temporaryPath,
    `// Generated by scripts/generate-worker-ghostty-assets.mjs. Do not edit by hand.\nimport { GHOSTTY_ASSET_PATHS } from "./ghostty-assets.js";\n\nexport const GHOSTTY_WORKER_ASSET_BASE64 = {\n${renderedAssets.join("\n")}\n} as const;\n`,
  );
  await rename(temporaryPath, generatedPath);
} finally {
  await rm(temporaryPath, { force: true });
}

/**
 * @param {string} distPath
 * @returns {Promise<string>}
 */
async function findBrowserExternalAsset(distPath) {
  const entries = await readdir(distPath);
  const matches = entries.filter((entry) =>
    /^__vite-browser-external-[A-Za-z0-9_-]+\.js$/.test(entry),
  );
  if (matches.length !== 1) {
    throw new Error(
      `expected one ghostty-web browser external asset in ${distPath}, found ${matches.length}`,
    );
  }
  return path.join(distPath, matches[0]);
}
