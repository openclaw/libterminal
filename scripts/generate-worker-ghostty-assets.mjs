import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const modulePath = fileURLToPath(import.meta.resolve("ghostty-web"));
const distPath = path.dirname(modulePath);
const generatedPath = new URL("../src/worker-ghostty-assets.generated.ts", import.meta.url);
const assets = [
  {
    key: "module",
    path: modulePath,
    contentType: "text/javascript; charset=utf-8",
  },
  {
    key: "wasm",
    path: fileURLToPath(import.meta.resolve("ghostty-web/ghostty-vt.wasm")),
    contentType: "application/wasm",
  },
  {
    key: "browserExternal",
    path: path.join(distPath, "__vite-browser-external-2447137e.js"),
    contentType: "text/javascript; charset=utf-8",
  },
];

const renderedAssets = await Promise.all(
  assets.map(async ({ key, path: assetPath, contentType }) => {
    const body = await readFile(assetPath);
    const base64 = JSON.stringify(body.toString("base64"));
    return [
      `  [GHOSTTY_ASSET_PATHS.${key}]: {`,
      ...(base64.length > 80 ? [`    base64:`, `      ${base64},`] : [`    base64: ${base64},`]),
      `    contentType: ${JSON.stringify(contentType)},`,
      `  },`,
    ].join("\n");
  }),
);

await writeFile(
  generatedPath,
  `// Generated by scripts/generate-worker-ghostty-assets.mjs. Do not edit by hand.\nimport { GHOSTTY_ASSET_PATHS } from "./ghostty-assets.js";\n\nexport const GHOSTTY_WORKER_ASSET_BASE64 = {\n${renderedAssets.join("\n")}\n} as const;\n`,
);
